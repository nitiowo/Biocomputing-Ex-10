# write a game for the user to guess a number between 1-100 randomly generated by the computer
# returns 'lower', 'higher', or 'correct!' based on the guess
# player can guess up to 10 times

print("I'm thinking of a number 1-100... You get 10 guesses!")
random.number <- sample(1:100, size=1)
for (i in 1:10){
  guess <- as.integer(readline(prompt = "Guess a number between 1-100: "))
  if (random.number<guess){
    print("lower")
    print(paste("Tries remaining:", 10 - i))
  } else if (random.number>guess){
    print("higher")
    print(paste("Tries remaining:", 10 - i))
  } else {
    print("correct!")
    break
  }
}
