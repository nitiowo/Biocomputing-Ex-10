# using score info from UWvMSU_1-22-13.txt generate a graph
# hint : generate a dataframe with a cumulative score for each team whenever either team scores
library(ggplot2)
setwd('/Users/laurenlatimer/Biocomputing/Biocomputing-Ex-10')
bball.data <- read.table("UWvMSU_1-22-13.txt", header=TRUE)
cumulative.scores <- data.frame(bball.data)

uw.score <- 0
msu.score <- 0 

for (i in 1:nrow(cumulative.scores)){
  if (cumulative.scores[i,2]=="UW"){
    uw.score <- uw.score + cumulative.scores[i, 3]
    cumulative.scores[i, 3] <- uw.score
  }
  else{
    msu.score <- msu.score + cumulative.scores[i, 3]
    cumulative.scores[i, 3] <- msu.score
  }
}

ggplot(cumulative.scores, aes(x = time, y = score, color = team)) +
  geom_step() +
  scale_color_manual(values = c("UW" = "red", "MSU" = "darkgreen")) +
  labs(title = "Michigan State vs. Wisconsin Score over Time",
       x = "Time",
       y = "Cumulative Score",
       color = "Team")

